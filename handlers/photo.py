@router.callback_query(ImageGenerationStates.waiting_confirmation, F.data.startswith("confirm_"))
async def confirm_generation(callback: CallbackQuery, state: FSMContext):
    """–ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ –∏ –∑–∞–ø—É—Å–∫ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏."""
    user_id = callback.from_user.id
    
    if callback.data == "confirm_1024x1024":
        size = "1024x1024" 
    elif callback.data == "confirm_1536x1024":
        size = "1536x1024"
    else:
        await callback.answer(
            f"‚ùå –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π —Ä–∞–∑–º–µ—Ä –∫–∞—Ä—Ç–∏–Ω–∫–∏",
            show_alert=True,
        )
        await state.clear()
        return

    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ —Å–æ—Å—Ç–æ—è–Ω–∏—è
    data = await state.get_data()
    image_bytes = data.get("image_bytes")
    prompt = data.get("prompt")

    if not image_bytes or not prompt:
        await callback.answer("‚ùå –û—à–∏–±–∫–∞: –¥–∞–Ω–Ω—ã–µ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã")
        await state.clear()
        return

    async with async_session_maker() as session:
        from sqlalchemy import select

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫—Ä–µ–¥–∏—Ç—ã –∏ —Å–ø–∏—Å—ã–≤–∞–µ–º
        result = await session.execute(select(User).where(User.id == user_id))
        user = result.scalar_one_or_none()

        if user is None or user.credits < settings.GENERATION_COST:
            await callback.answer(
                f"‚ùå –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∫—Ä–µ–¥–∏—Ç–æ–≤. –ù—É–∂–Ω–æ {settings.GENERATION_COST}",
                show_alert=True,
            )
            await state.clear()
            return

        # –°–ø–∏—Å—ã–≤–∞–µ–º –∫—Ä–µ–¥–∏—Ç—ã
        user.credits -= settings.GENERATION_COST
        await session.commit()

        # –ó–∞–ø—É—Å–∫–∞–µ–º –≥–µ–Ω–µ—Ä–∞—Ü–∏—é
        client = ImageGenerationClient()
        response = await client.generate_image(
            image_bytes=BytesIO(image_bytes).read(),
            prompt=prompt,
            model="gpt-image-1",
            size=size,
        )

        if response and response.get("request_id"):
            request_id = str(response["request_id"])
            
            # –°–æ–∑–¥–∞–µ–º –∑–∞–¥–∞—á—É –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏
            from database.models import GenerationTask

            task = await create_generation_task(
                session=session,
                user_id=user_id,
                chat_id=callback.message.chat.id,
                request_id=request_id,
                prompt=prompt,
                model="gpt-image-1",
                size=size,
            )

            await callback.message.edit_text(
                f"‚è≥ –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –∑–∞–ø—É—â–µ–Ω–∞!\n\n"
                f"üí≥ –°–ø–∏—Å–∞–Ω–æ: {settings.GENERATION_COST} –∫—Ä–µ–¥–∏—Ç–∞(–æ–≤)\n"
                f"üí∞ –û—Å—Ç–∞–ª–æ—Å—å –∫—Ä–µ–¥–∏—Ç–æ–≤: {user.credits}\n\n"
                f"üîÑ –û–∂–∏–¥–∞–π—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç..."
            )
            await callback.answer("–ì–µ–Ω–µ—Ä–∞—Ü–∏—è –∑–∞–ø—É—â–µ–Ω–∞!")
        else:
            # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –∫—Ä–µ–¥–∏—Ç—ã –ø—Ä–∏ –æ—à–∏–±–∫–µ
            user.credits += settings.GENERATION_COST
            await session.commit()

            await callback.message.edit_text(
                "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏. –ö—Ä–µ–¥–∏—Ç—ã –≤–æ–∑–≤—Ä–∞—â–µ–Ω—ã. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ."
            )
            await callback.answer("–û—à–∏–±–∫–∞ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏", show_alert=True)

    await state.clear()


