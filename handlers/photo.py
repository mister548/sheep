@router.message(ImageGenerationStates.waiting_prompt, F.text)
async def process_prompt(message: Message, state: FSMContext):
    """–û–±—Ä–∞–±–æ—Ç–∫–∞ prompt –∏ –∑–∞–ø—Ä–æ—Å –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è."""
    prompt = message.text
    user_id = message.from_user.id

    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ —Å–æ—Å—Ç–æ—è–Ω–∏—è
    data = await state.get_data()
    image_bytes = data.get("image_bytes")

    if not image_bytes:
        await message.answer("‚ùå –û—à–∏–±–∫–∞: –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ. –ù–∞—á–Ω–∏—Ç–µ –∑–∞–Ω–æ–≤–æ —Å /photo")
        await state.clear()
        return

    # –°–æ—Ö—Ä–∞–Ω—è–µ–º prompt
    await state.update_data(prompt=prompt)

    # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –¥–ª—è –≤—ã–±–æ—Ä–∞ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ (–ø–æ –∂–µ–ª–∞–Ω–∏—é)
    keyboard = InlineKeyboardMarkup(
        inline_keyboard=[
            [
                InlineKeyboardButton(
                    text="‚úÖ –ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç—å (1024x1024)", callback_data="confirm_1024x1024"
                ),
                InlineKeyboardButton(
                    text="‚úÖ –ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç—å (1536x1024)", callback_data="confirm_1536x1024"
                ),
            ]
        ]
    )

    await state.set_state(ImageGenerationStates.waiting_confirmation)
    await message.answer(
        f"üìã –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã:\n\n"
        f"üìù Prompt: {prompt}\n"
        f"üé® –ú–æ–¥–µ–ª—å: gpt-image-1\n"
        f"üí≥ –°—Ç–æ–∏–º–æ—Å—Ç—å: {settings.GENERATION_COST} –∫—Ä–µ–¥–∏—Ç–∞(–æ–≤)\n\n"
        f"–í—ã–±–µ—Ä–∏—Ç–µ —Ä–∞–∑–º–µ—Ä –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è:",
        reply_markup=keyboard,
    )


